package BAEKJOON;

import java.io.*;

public class p1916 {
    //문제풀이
    //1. 전형적인 다익스트라문제
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(System.out));

        bw.flush();
        bw.close();
        br.close();
    }
}
